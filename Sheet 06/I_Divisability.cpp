ll sum(ll num) {
    return num*(num+1)/2;
}
void solve()
{
    ll a,b,x; cin >> a >> b >> x;
    ll mx=max(a,b);
    ll mn=min(a,b);
    ll res=(sum(mx/x)*x)-(sum((mn-1)/x)*x);
    cout << res << endl;
}


